    <div class="article-copy">
      <div class="section-title">Learned Lessons</div>
      <p>
        I learned a few things from this project and reinforced existing practise
        methods. Firstly to get reliable results from split testing, you have to
        use a larger percentage of traffic. Stakeholders were hesitant to send so
        much traffic to the new page to begin with, this delayed getting the results
        we needed and therefore held the end result up. Part way through the test I
        was fortunate enough to grab <a href="https://twitter.com/multimichel" target="_blank">@multimichel</a> for a chat at Generate Conference,
        Michel was a speaker at the conference and is a designer at Booking.com and he reinforced my point about traffic levels in AB testing.
        </br></br>
        I knew backing up designs and briefs with data was important but this project
        really strengthened that fact. Some of the stakeholders involved with the
        project had been with the company for a while and were therefore very hard
        to convince on changes. Having data to back us up every step of the way was an
        important method to protect ourselves from being directed by the HiPPO,
        otherwise known as the ‘highest paid person's opinion’.
      </p>
