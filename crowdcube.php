    <div class="article-copy">
      <p>
        A lot of the site has and is still undergoing this visual update therefore
        I will just focus on the homepage for this case study. There were 2 goals
        we had for this project: make the page more aesthetically pleasing and
        reduce the amount of unnecessary information. Although this project was
        further down the roadmap it was something I was keen to attack as soon as
        possible, therefore I started collecting data from Google Analytics and
        meeting with our Data Analyst to get insight on what elements were being
        engaged with and what was a waste of real estate. I then created a quick
        concept design on the side to spark interest and conversations with stakeholders.
        Although I knew aspects of the concept would not make it into the project it
        was the best way to get people excited about the prospect of a new homepage.</br></br>
        Once a plan of attack and brief had been created with product I went about
        creating a new version of the homepage which was to be tested against the
        existing homepage. The new version proved to be a success by positively
        affecting our primary goal of increasing sign ups, going from 1.38% to 2.92%.
        It also achieved it’s goal of not damaging other metrics but also improved on
        those metrics. Click throughs to ‘Investment Opportunities’ was increased from
        43.73% to 51.81% and bounce rate was decreased from 30.22% to 27.51% which are
        both major achievements. This data allowed us to justify pushing all traffic to
        the new design.
      </p>
