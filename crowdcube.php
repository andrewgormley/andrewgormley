    <div class="article-copy">
      <div class="section-title">The Solution</div>
      <p>
        Due to the our resource and time constraints we decided to make the visual
        changes a very gradual project. This involved lot’s of small releases but
        done quickly. And for the larger changes using split tests against the originals
        to ensure our users engagement was not affected. To help combat the amount of
        customs code the Front-End team were churning out we decided to work on a
        component library which would allow them to reuse elements across the site,
        this was time consuming to set up as expected but we quickly started seeing the
        benefits.
      </p>
    </div>

