      <div class="section-title">The Solution</div>
      <p>
        As a team we came up with the idea to take advantage of the parts of the
        site that were not tied to the existing framework. Mainly the homepage,
        other landing pages and the art direction of the product photography.
        This allowed us to take control through the creative direction of the site.</br></br>
        Very early on the contents of each page were decided and a sitemap was created,
        which left us with sufficient time to nail the designs. This was achieved through
        mood boarding, white board sketching and constantly running open discussions about our concept designs.
      </p>
    </div>

