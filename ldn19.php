      <div class="section-title">The Build</div>
      <p>
        This project posed different challenges from the usual web builds.
        Even though it was one big web application, the viewport never changed size and
        touch gestures were the only way to navigate and interact with content.
        HTML and CSS were my main tools to get the basic functionality and aesthetics of the pages,
        through directly watching our users we quickly found the most engaging content utilised animations and interactive content.</br></br>
        To tackle this issue I took advantage of existing libraries such as Animate.CSS and WOW.js
        for the animations and a basic understanding of JavaScript to allow for functions on click (or touch in this case).
        The content was the most important thing therefore it was key to not overdo the bells and whistles on each feature.
      </p>
